package com.smart.pay.models.output;

public class BusSearchResult {

    String travelsName;
    String busPrice;
    String busType;
    String busArrivalTime;
    String busSeats;
    String busTotalDuration;
    String busReachTime;
    String busRatings;
    String busTotalRatings;

    public String getTravelsName() {
        return travelsName;
    }

    public void setTravelsName(String travelsName) {
        this.travelsName = travelsName;
    }

    public String getBusPrice() {
        return busPrice;
    }

    public void setBusPrice(String busPrice) {
        this.busPrice = busPrice;
    }

    public String getBusType() {
        return busType;
    }

    public void setBusType(String busType) {
        this.busType = busType;
    }

    public String getBusArrivalTime() {
        return busArrivalTime;
    }

    public void setBusArrivalTime(String busArrivalTime) {
        this.busArrivalTime = busArrivalTime;
    }

    public String getBusSeats() {
        return busSeats;
    }

    public void setBusSeats(String busSeats) {
        this.busSeats = busSeats;
    }

    public String getBusTotalDuration() {
        return busTotalDuration;
    }

    public void setBusTotalDuration(String busTotalDuration) {
        this.busTotalDuration = busTotalDuration;
    }

    public String getBusReachTime() {
        return busReachTime;
    }

    public void setBusReachTime(String busReachTime) {
        this.busReachTime = busReachTime;
    }

    public String getBusRatings() {
        return busRatings;
    }

    public void setBusRatings(String busRatings) {
        this.busRatings = busRatings;
    }

    public String getBusTotalRatings() {
        return busTotalRatings;
    }

    public void setBusTotalRatings(String busTotalRatings) {
        this.busTotalRatings = busTotalRatings;
    }
}
