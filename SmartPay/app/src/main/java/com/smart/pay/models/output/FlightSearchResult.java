package com.smart.pay.models.output;

public class FlightSearchResult {


    String airlineName;
    String strAirline;
    String flightArrivalTime;
    String flightArrivalStation;
    String flightTotalDuration;
    String flightDepartureTime;
    String flightDepartureStation;
    String flightPrice;

    public String getAirlineName() {
        return airlineName;
    }

    public void setAirlineName(String airlineName) {
        this.airlineName = airlineName;
    }

    public String getStrAirline() {
        return strAirline;
    }

    public void setStrAirline(String strAirline) {
        this.strAirline = strAirline;
    }

    public String getFlightArrivalTime() {
        return flightArrivalTime;
    }

    public void setFlightArrivalTime(String flightArrivalTime) {
        this.flightArrivalTime = flightArrivalTime;
    }

    public String getFlightArrivalStation() {
        return flightArrivalStation;
    }

    public void setFlightArrivalStation(String flightArrivalStation) {
        this.flightArrivalStation = flightArrivalStation;
    }

    public String getFlightTotalDuration() {
        return flightTotalDuration;
    }

    public void setFlightTotalDuration(String flightTotalDuration) {
        this.flightTotalDuration = flightTotalDuration;
    }

    public String getFlightDepartureTime() {
        return flightDepartureTime;
    }

    public void setFlightDepartureTime(String flightDepartureTime) {
        this.flightDepartureTime = flightDepartureTime;
    }

    public String getFlightDepartureStation() {
        return flightDepartureStation;
    }

    public void setFlightDepartureStation(String flightDepartureStation) {
        this.flightDepartureStation = flightDepartureStation;
    }

    public String getFlightPrice() {
        return flightPrice;
    }

    public void setFlightPrice(String flightPrice) {
        this.flightPrice = flightPrice;
    }

}
